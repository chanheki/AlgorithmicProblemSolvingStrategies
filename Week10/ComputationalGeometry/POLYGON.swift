//  예를 들면 두 볼록 다각형의 교집합은 항상 볼록 다각형이라거나, 볼록 다각형 내부의 두 점을
// 연결하는 선분은 볼록 다각형의 경계를 절대 교차하지 않는다는 것 등

// 오목 다각형 (concave polygon) - 반대로 180도를 넘는 내각을 갖는 다각형

